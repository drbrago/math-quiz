import type { RealExamPoints, RealExamQuestion, RealExamQuestionPart } from "./questionTypes";

function sumPoints(parts: RealExamQuestionPart[]): RealExamPoints {
  return parts.reduce(
    (acc, part) => ({
      method: acc.method + part.rubric.method,
      reasoning: acc.reasoning + part.rubric.reasoning,
      communication: acc.communication + part.rubric.communication,
    }),
    { method: 0, reasoning: 0, communication: 0 },
  );
}

function createQuestion(data: Omit<RealExamQuestion, "mode" | "totalPoints">): RealExamQuestion {
  return {
    ...data,
    mode: "realExam",
    totalPoints: sumPoints(data.parts),
  };
}

